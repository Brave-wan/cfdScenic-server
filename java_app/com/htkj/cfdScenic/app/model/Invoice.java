package com.htkj.cfdScenic.app.model;

import com.htrj.common.base.BaseEntity;

public class Invoice extends BaseEntity{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Long id;
	private String payUnitName;//付款单位名称
	private String einNumber;//税号
	private String invoiceNumber;//发票号
	private Double invoiceMoney;//开票金额
	private String invoiceTime;//开发票时间
	private Integer invoiceState;//发票状态 1已开2待开
	private String orderCode;//订单号
	private Long linkId;//景区或者店铺ID
	private String createTime;//创建时间
	private Integer type;//发票类型 0景区 1商铺
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getPayUnitName() {
		return payUnitName;
	}
	public void setPayUnitName(String payUnitName) {
		this.payUnitName = payUnitName;
	}
	public String getEinNumber() {
		return einNumber;
	}
	public void setEinNumber(String einNumber) {
		this.einNumber = einNumber;
	}
	public String getInvoiceNumber() {
		return invoiceNumber;
	}
	public void setInvoiceNumber(String invoiceNumber) {
		this.invoiceNumber = invoiceNumber;
	}
	public Double getInvoiceMoney() {
		return invoiceMoney;
	}
	public void setInvoiceMoney(Double invoiceMoney) {
		this.invoiceMoney = invoiceMoney;
	}
	public String getInvoiceTime() {
		return invoiceTime;
	}
	public void setInvoiceTime(String invoiceTime) {
		this.invoiceTime = invoiceTime;
	}
	public Integer getInvoiceState() {
		return invoiceState;
	}
	public void setInvoiceState(Integer invoiceState) {
		this.invoiceState = invoiceState;
	}

	
	public String getOrderCode() {
		return orderCode;
	}
	public void setOrderCode(String orderCode) {
		this.orderCode = orderCode;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Long getLinkId() {
		return linkId;
	}
	public void setLinkId(Long linkId) {
		this.linkId = linkId;
	}
	
}
